use specta_typescript::{BigIntExportBehavior, Typescript};
use tauri_specta::{collect_commands, Builder};

fn main() {
    let out_path = "frontend/src/bindings/index.ts";

    let builder = Builder::<tauri::Wry>::new()
        .commands(collect_commands![
            projectpacker_app_lib::commands::pack_start,
            projectpacker_app_lib::commands::pack_cancel,
            projectpacker_app_lib::commands::pack_get_result,
            projectpacker_app_lib::commands::validate_plan,
            projectpacker_app_lib::commands::build_combined_prompt,
            projectpacker_app_lib::commands::get_settings,
            projectpacker_app_lib::commands::save_settings,
            projectpacker_app_lib::commands::save_to_file,
        ])
        .typ::<projectpacker_core::types::ProgressEvent>()
        .typ::<projectpacker_core::tokens::TokenModel>()
        .typ::<projectpacker_core::tokens::TokensPerModel>()
        .typ::<projectpacker_core::secrets::Redaction>()
        .typ::<projectpacker_core::types::PackRedaction>();

    builder
        .export(
            Typescript::default()
                .bigint(BigIntExportBehavior::Number)
                .header(
                    "// Auto-generated by ProjectPacker — do not edit.\n/* eslint-disable */\n",
                ),
            out_path,
        )
        .expect("Failed to export typescript bindings");

    // tauri-specta emits `export type TAURI_CHANNEL<TSend> = null` as a
    // placeholder, then imports `Channel as TAURI_CHANNEL` from
    // @tauri-apps/api/core in the same file. TypeScript 5 rejects the
    // duplicate declaration (TS2440). Strip the conflicting type alias so
    // the import wins unambiguously.
    let contents =
        std::fs::read_to_string(out_path).expect("Failed to read generated bindings");
    let patched = contents
        .lines()
        .filter(|line| !line.starts_with("export type TAURI_CHANNEL<"))
        .collect::<Vec<_>>()
        .join("\n");
    // Preserve trailing newline
    let patched = format!("{patched}\n");
    std::fs::write(out_path, patched).expect("Failed to patch generated bindings");
}
